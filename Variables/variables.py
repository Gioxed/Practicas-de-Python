#deifnir variable
nombre = "Gio"

#concatenar con f-Strings
bienvenida = f"Hola {nombre} ¿Cómo estás?" #transforma cualquier dato a string

#contatenar con +
bienvenida = "Hola " + nombre +  " ¿Cómo estás?"

#del bienvenida borra el valor de la variable


#operadores de pertenencia (in y not in)
print("Gio" in bienvenida) #true
print("Gio" not in bienvenida) #false